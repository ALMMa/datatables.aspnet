namespace DataTables.AspNet.Core
{
    /// <summary>
    /// Defines directions for column sorting.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// Represents an ascendant sorting (default).
        /// </summary>
        Ascending = 0,
        /// <summary>
        /// Represents a descendant sorting.
        /// </summary>
        Descending = 1
    }
}
