namespace DataTables.AspNet.Core
{
    /// <summary>
    /// Defines directions for column sorting.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// Defines an ascendant sorting (default).
        /// </summary>
        Ascending = 0,

        /// <summary>
        /// Defines a descendant sorting.
        /// </summary>
        Descending = 1
    }
}
